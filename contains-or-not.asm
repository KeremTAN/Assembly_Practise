org 100h

LEA SI,A1
LEA DI,A2
MOV DL,LENGTH1
MOV CL,LENGTH2
CMP CL,DL
JA DOESNOTCONTAIN    
JE LENGTHISEQ 
JB COMPARE


LENGTHISEQ:
REPE CMPSB
JNE DOESNOTCONTAIN
JMP CONTAINS

COMPARE:
MOV AL,[SI]
MOV AH,[DI] 
CMP AL,AH
JE CHECK      
    
ARETHEREMORECHAR:
INC SI 
DEC DL
CMP DL, 0000H
JE DOESNOTCONTAIN
JMP COMPARE
    
CHECK:
MOV CL, LENGTH2   
MOV SP, SI
ADD SP, 0001H
CLD
REPE CMPSB
JE CONTAINS    

NEXTCHAR:            
MOV SI,SP 
DEC DL
LEA DI, A2
JMP COMPARE
    
CONTAINS:  
LEA SI,A1
MOV CL,LENGTH1
MOV AH,0Eh
DISP:
LODSB
INT 10H
LOOP DISP  
                          
             
LEA SI,CONT
MOV CL,CONTLEN   
MOV AH,0Eh
DISP2:
LODSB
INT 10H
LOOP DISP2
              
                            
LEA SI,A2
MOV CL,LENGTH2  
MOV AH,0Eh
DISP3:
LODSB
INT 10H
LOOP DISP3 
     
JMP EXIT    


DOESNOTCONTAIN:    
LEA DI,NOTCONT   
MOV DL,NOTCONTLEN 
CALL PRINT
JMP EXIT  
        
     
PRINT PROC  
      
LEA SI,A1
MOV CL,LENGTH1
MOV AH,0Eh
DISP11:
LODSB
INT 10H
LOOP DISP11  
                          
             
MOV SI,DI
MOV CL,DL   
MOV AH,0Eh
DISP12:
LODSB
INT 10H
LOOP DISP12
              
                            
LEA SI,A2
MOV CL,LENGTH2  
MOV AH,0Eh
DISP13:
LODSB
INT 10H
LOOP DISP13  
   
 
RET         
PRINT ENDP  
           
EXIT:

ret

A1 DB 'M','I','C','R','O','C','O','M','P','U','T','E','R','S'
A2 DB 'M','I','C','R','O'
LENGTH1 DB 14
LENGTH2 DB 5
CONT DB ' CONTAINS '
NOTCONT DB ' DOES NOT CONTAIN '     
CONTLEN DB 10
NOTCONTLEN DB 18